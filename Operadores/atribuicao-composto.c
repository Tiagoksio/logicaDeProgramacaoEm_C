#include <stdio.h>

void main (){
    
    int num;
    
    num = 1; // atribui o valor 1 à variável 
    printf("Atribuindo 1 a num = %d\n", num);
    
    num += 1; // num = num+1 
    printf("Somando 1 a num = %d\n", num);
    
    num -= 1; // num = num-1 
    printf("Subtraindo 1 de num = %d\n", num); 
    
    num *= 2; // num = num*2
    printf("MUltiplicando num por 2 = %d\n", num); 
    
    num /= 2; // num = num/2 
    printf("Dividindo num por 2 = %d\n", num); 
       
}
   

