#include <stdio.h>

int main(){
    
    printf("alarme sonoro\a");//\a - Alarme sonoro do sistema;
    printf("Move o cursor uma posição à esquerda\b");//\b - Move o cursor uma posição à esquerda;
    printf("Próxima linha\n");//\n - Próxima linha;
    printf("tabulação\t");//\t - tabulação;
    printf("início da linha\r");//\r - início da linha;
    printf("Caractere nulo\0");//\0 - Caractere nulo, fim da string;
    printf("\"Escreve entre aspas\"\0");//\" - entre aspas;
    return 0;
}
